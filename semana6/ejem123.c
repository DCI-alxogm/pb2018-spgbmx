#include<stdio.h>

int main ()
{
	int i, j, N;
	float numeros[i];

	for(i=0;i<N;i++)
	{
		numeros[i]=0;//Se le asigna el valor de cero a todos los elementos del arreglo
	}
	for(i=0;i<N;i++)
	{
		scanf("%f",&numeros[i]);//Asigna valores a cada elemento del arreglo
	}
	for(j=0;j<N;j++)
	{
		printf("%f\n",numeros[i]);//Imprime todos los valores del arreglo
		
	}
return 0;

}
