#include<stdio.h>

int main ()
{
	int i, N;
	float numeros[i];

	for(i=0;i<N;i++)
	{
		scanf("%f",&numeros[i]);//Asigna el valor al arreglo.
		numeros[i]*=2;//MultiplicaciÃ³n del arreglo por 2
		printf("%f\n",numeros[i]);//imprime al arreglo modificado por la multiplicacion
	}
}
